package lambda;

public class Student {
	private String sn;
	private int sc;
	
	
	public Student(String sn, int sc) {
		super();
		this.sn = sn;
		this.sc = sc;
	}
	public String getSn() {
		return sn;
	}
	public void setSn(String sn) {
		this.sn = sn;
	}
	public int getSc() {
		return sc;
	}
	public void setSc(int sc) {
		this.sc = sc;
	}
	
	
}
